<?php

// Include config file
require_once "config.php";
 
// Define variables and initialize with empty values
$username = $password = $confirm_password = "";
$username_err = $password_err = $confirm_password_err = "";
 
// Processing form data when form is submitted
if($_SERVER["REQUEST_METHOD"] == "POST"){
 
    // Validate username
    // This trims the white space off of the variable "Username" so there is no white space atached to it. 
    if(empty(trim($_POST["username"]))){
        $username_err = "Please enter a username.";
    } 
    
    elseif(!preg_match('/^[a-zA-Z0-9_]+$/', trim($_POST["username"]))){
        $username_err = "Username can only contain letters, numbers, and underscores.";
    } // This compares each character of username to see if it matches with the specific characters. 
    else{
        // Prepare a select statement

        // This whole section of php code is to determine if the user name and password entered are new and not duplicates. 
        $sql = "SELECT id FROM users WHERE username = ?";
        
        // $link comes from the config.php file which connects to the database. 
        //$stmt is a statement object retured from the prepare. 
        // the $sql is sent over to the server where it performs a syntax check before things move forward.
        // the '?' is an anonymous positional placeholder, there will be a variable subbed in later for the '?'
        if($stmt = mysqli_prepare($link, $sql)){
            // Bind variables to the prepared statement as parameters
            mysqli_stmt_bind_param($stmt, "s", $param_username);
            
            // Set parameters
            // Sets the value that will be subbed in for the '?'
            $param_username = trim($_POST["username"]);
            
            // Attempt to execute the prepared statement
            // This returns true or false if it works. 
            if(mysqli_stmt_execute($stmt)){
                /* store result */
                mysqli_stmt_store_result($stmt);
                
                if(mysqli_stmt_num_rows($stmt) == 1){ // This means that there is already a row with that username in it. 
                    $username_err = "This username is already taken.";
                } else{
                    $username = trim($_POST["username"]);
                }
            } else{
                echo "Oops! Something went wrong. Please try again later.";
            }

            // Close statement
            mysqli_stmt_close($stmt);
        }
    }
    
    // Validate password
    if(empty(trim($_POST["password"]))){
        $password_err = "Please enter a password.";     
    } elseif(strlen(trim($_POST["password"])) < 6){
        $password_err = "Password must have atleast 6 characters.";
    } else{
        $password = trim($_POST["password"]);
    }
    
    // Validate confirm password
    if(empty(trim($_POST["confirm_password"]))){
        $confirm_password_err = "Please confirm password.";     
    } else{
        $confirm_password = trim($_POST["confirm_password"]);
        if(empty($password_err) && ($password != $confirm_password)){
            $confirm_password_err = "Password did not match.";
        }
    }
    
    // Check input errors before inserting in database
    // This inserts the username and passsword into the database. 
    if(empty($username_err) && empty($password_err) && empty($confirm_password_err)){ // No password errors -> password error variable is empty. 
        
        // Prepare an insert statement
        $sql = "INSERT INTO users (username, password) VALUES (?, ?)";
         
        if($stmt = mysqli_prepare($link, $sql)){
            // Bind variables to the prepared statement as parameters
            // The 'ss' is the format of which the two values username and password are being passed in and substituted for the '?'
            mysqli_stmt_bind_param($stmt, "ss", $param_username, $param_password);
            
            // Set parameters
            $param_username = $username;
            $param_password = password_hash($password, PASSWORD_DEFAULT); // Creates a password hash
            
            // Attempt to execute the prepared statement
            if(mysqli_stmt_execute($stmt)){
                // If the statement is executed, then redirect the user to the login page. 
                header("location: login.php");
            } else{
                echo "Oops! Something went wrong. Please try again later.";
            }

            // Close statement
            mysqli_stmt_close($stmt);
        }
    }
    
    // Close connection
    mysqli_close($link);
}
